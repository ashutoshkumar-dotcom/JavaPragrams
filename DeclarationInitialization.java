package com.list.programs;

public class DeclarationInitialization {

	public static void main(String[] args) {
			
		// Declaration
		byte by;
		long lon;
		short sh;
		int i;
		float f;
		double d;
		char ch;
		boolean b;
		String st;
		Object obj;
				
		//Initialization
		by = 126;
		lon = 943202033;
		sh = 32766;
		i = 5;
		f = 1.2f;
		d = .0038503;
		ch = 'A';
		b = true;
		st = "Ashutosh";
		obj = "test";
		System.out.println("Declaration & Initializations : \n"+by +"\n" +lon +"\n" +sh +"\n" +i +"\n" +f +"\n" +d +"\n" +ch +"\n" +b +"\n"+st +"\n" +obj);

	}

}
